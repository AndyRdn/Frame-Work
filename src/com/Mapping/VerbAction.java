package com.Mapping;

public class VerbAction {
    String action;
    String verb;

    public VerbAction(String action, String verb) {
        this.action = action;
        this.verb = verb;
    }

    public String getAction() {
        return action;
    }

    public void setAction(String action) {
        this.action = action;
    }

    public String getVerb() {
        return verb;
    }

    public void setVerb(String verb) {
        this.verb = verb;
    }

}
